#include "libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	char	*temp;
	int		i;

	i = 0;
	if (s == 0 && f == 0)
		return (0);
	temp = (char *)malloc(ft_strlen(s) + 1);
	if (temp == 0)
		return (0);
	while (s[i])
	{
		temp[i] = f(i, s[i]);
		i++;
	}
	temp[i] = '\0';
	return (temp);
}
